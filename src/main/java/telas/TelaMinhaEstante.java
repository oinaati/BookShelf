    private void detalharButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_detalharButtonActionPerformed
        int linha = estanteTable.getSelectedRow();

        try {
            if (linha >= 0) {
                nomejLabel.setText((String) estanteTable.getValueAt(linha, 1));
                autorjLabel.setText((String) estanteTable.getValueAt(linha, 2));
                generojLabel.setText((String) estanteTable.getValueAt(linha, 3));
                paginasjLabel.setText(Integer.toString((int) estanteTable.getValueAt(linha, 4)));
                paginasLidasjLabel.setText(Integer.toString((int) estanteTable.getValueAt(linha, 5)));
                notajLabel.setText(Integer.toString((int) estanteTable.getValueAt(linha, 6)));

                int lidas = (int) estanteTable.getValueAt(linha, 5);
                int total = (int) estanteTable.getValueAt(linha, 4);

                double calculo = (lidas * 100) / total;

                progressojLabel.setText(Double.toString(calculo) + "%");
            } else {
                JOptionPane.showMessageDialog(null, "Selecione um livro");
            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Não foi possivel mostrar as informações do livro! " + ex);
            ex.printStackTrace();
        }
    }//GEN-LAST:event_detalharButtonActionPerformed

