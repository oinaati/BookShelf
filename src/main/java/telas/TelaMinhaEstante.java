    private void removerButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removerButtonActionPerformed
        int linha = estanteTable.getSelectedRow();
        if (linha >= 0) {
            int id = (int) estanteTable.getValueAt(linha, 0);

            Livros livro = new Livros(id);
            try {
                DAOEstante.removerEstante(livro);
                ((DefaultTableModel) estanteTable.getModel()).removeRow(estanteTable.getSelectedRow());
                JOptionPane.showMessageDialog(null, "Livro removido com sucesso!");
                nomejLabel.setText("-");
                autorjLabel.setText("-");
                generojLabel.setText("-");
                paginasjLabel.setText("-");
                paginasLidasjLabel.setText("-");
                notajLabel.setText("-");
                progressojLabel.setText("-");

            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, "Erro ao remover livro, tente novamente mais tarde... " + ex);
                ex.printStackTrace();
            }
        } else {
            JOptionPane.showMessageDialog(null, "Selecione um livro");
        }
    }//GEN-LAST:event_removerButtonActionPerformed

    private void progressoButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_progressoButtonActionPerformed
        int linha = estanteTable.getSelectedRow();

        try {
            if (linha >= 0) {
                int id = (int) estanteTable.getValueAt(linha, 0);
                int paginasLidas = (int) progressoSpinner.getValue();

                int paginas = (int) estanteTable.getValueAt(linha, 4);

                Livros livros = new Livros(id, paginasLidas);

                if (paginasLidas >= 0) {
                    if (paginasLidas > paginas) {
                        JOptionPane.showMessageDialog(null, "Não tem como ler mais páginas do que o livro possui.");
                    } else {
                        DAOEstante.progressoLivro(livros, id);
                        progressoSpinner.setValue(0);
                        JOptionPane.showMessageDialog(null, "A leitura sempre agrega no nosso conhecimento.");
                        reiniciaTela();
                    }
                }

            } else {
                JOptionPane.showMessageDialog(null, "Selecione um livro");
            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Teste novamente mais tarde! " + ex);
            ex.printStackTrace();
        }


    }//GEN-LAST:event_progressoButtonActionPerformed

