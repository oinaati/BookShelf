    private void notaButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_notaButtonActionPerformed

        String notaZero = zeroRadioButton.getText();
        String notaHum = humRadioButton.getText();
        String notaDois = doisRadioButton.getText();
        String notaTres = tresRadioButton.getText();
        String notaQuatro = quatroRadioButton.getText();
        String notaCinco = cincoRadioButton.getText();

        int linha = estanteTable.getSelectedRow();

        try {
            if (linha >= 0) {
                int id = (int) estanteTable.getValueAt(linha, 0);
                Livros notaLivros = new Livros(id);

                if (zeroRadioButton.isSelected()) {
                    notaLivros.setNota(Integer.parseInt(notaZero));
                    DAOEstante.avaliarLivro(notaLivros, id);
                    JOptionPane.showMessageDialog(null, "Nota atribuida, valeu!");
                    reiniciaTela();
                } else if (humRadioButton.isSelected()) {
                    notaLivros.setNota(Integer.parseInt(notaHum));
                    DAOEstante.avaliarLivro(notaLivros, id);
                    JOptionPane.showMessageDialog(null, "Nota atribuida, valeu!");
                    reiniciaTela();
                } else if (doisRadioButton.isSelected()) {
                    notaLivros.setNota(Integer.parseInt(notaDois));
                    DAOEstante.avaliarLivro(notaLivros, id);
                    JOptionPane.showMessageDialog(null, "Nota atribuida, valeu!");
                    reiniciaTela();
                } else if (tresRadioButton.isSelected()) {
                    notaLivros.setNota(Integer.parseInt(notaTres));
                    DAOEstante.avaliarLivro(notaLivros, id);
                    JOptionPane.showMessageDialog(null, "Nota atribuida, valeu!");
                    reiniciaTela();
                } else if (quatroRadioButton.isSelected()) {
                    notaLivros.setNota(Integer.parseInt(notaQuatro));
                    DAOEstante.avaliarLivro(notaLivros, id);
                    JOptionPane.showMessageDialog(null, "Nota atribuida, valeu!");
                    reiniciaTela();
                } else if (cincoRadioButton.isSelected()) {
                    notaLivros.setNota(Integer.parseInt(notaCinco));
                    DAOEstante.avaliarLivro(notaLivros, id);
                    JOptionPane.showMessageDialog(null, "Nota atribuida, valeu!");
                    reiniciaTela();
                } else {
                    JOptionPane.showMessageDialog(null, "Selecione a sua nota.");
                }

            } else {
                JOptionPane.showMessageDialog(null, "Selecione um livro.");
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Tente novamente mais tarde! " + ex);
            ex.printStackTrace();
        }


    }//GEN-LAST:event_notaButtonActionPerformed

