    private void removerButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removerButtonActionPerformed
        int linha = bibliotecaTable.getSelectedRow();
        if (linha >= 0) {
            int id = (int) bibliotecaTable.getValueAt(linha, 0);

            Livros livro = new Livros(id);
            try {
                DAOLivros.removerBiblioteca(livro);
                ((DefaultTableModel) bibliotecaTable.getModel()).removeRow(bibliotecaTable.getSelectedRow());
                JOptionPane.showMessageDialog(null, "Livro removido com sucesso!");

            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, "Erro ao remover livro, tente novamente mais tarde... " + ex);
                ex.printStackTrace();
            }
        } else {
            JOptionPane.showMessageDialog(null, "Selecione um livro");
        }


    }//GEN-LAST:event_removerButtonActionPerformed

