    private void editarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editarButtonActionPerformed

        int linha = bibliotecaTable.getSelectedRow();
        if (linha >= 0) {
            int id = (int) bibliotecaTable.getValueAt(linha, 0);
            String nome = (String) bibliotecaTable.getValueAt(linha, 1);
            String autor = (String) bibliotecaTable.getValueAt(linha, 2);
            String genero = (String) bibliotecaTable.getValueAt(linha, 3);
            int paginas = (int) bibliotecaTable.getValueAt(linha, 4);

            TelaEditarLivros viewEdit = new TelaEditarLivros(id, nome, autor, genero, paginas);
            viewEdit.setVisible(true);

        } else {
            JOptionPane.showMessageDialog(null, "Selecione um livro");
        }


    }//GEN-LAST:event_editarButtonActionPerformed

    private void addEstanteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addEstanteButtonActionPerformed
        int linha = bibliotecaTable.getSelectedRow();
        if (linha >= 0) {

            String nome = (String) bibliotecaTable.getValueAt(linha, 1);
            String autor = (String) bibliotecaTable.getValueAt(linha, 2);
            String genero = (String) bibliotecaTable.getValueAt(linha, 3);
            int paginas = (int) bibliotecaTable.getValueAt(linha, 4);

            String usuarioLogado = (String) jLabelUsuario.getText();

            Livros livros = new Livros(nome, autor, genero, paginas);
            Usuarios usuario = new Usuarios(usuarioLogado);

            try {
                DAOEstante.adicionarEstante(livros, usuario);
                JOptionPane.showMessageDialog(null, "Livro adicionado em sua estante!");
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, "Não foi possivel adicionar esse livro a sua estante." + ex);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Selecione um livro");
        }
    }//GEN-LAST:event_addEstanteButtonActionPerformed

